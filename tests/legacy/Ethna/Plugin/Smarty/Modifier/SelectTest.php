class Ethna_Plugin_Smarty_Modifier_SelectTest extends PHPUnit_Framework_TestCase
{
    public function test_smarty_modifier_select()
    {
        $r = smarty_modifier_select('a', 'b');
        $this->assertNull($r);

        $r = smarty_modifier_select('a', 'a');
        $this->assertEquals($r, 'selected="selected"');
    }

}

